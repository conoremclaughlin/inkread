#!/usr/bin/env node
/**
 * Generates the bundled sample PDF (a public-domain excerpt) and embeds it
 * as base64 in src/assets/samplePdf.ts. Committed output — run manually if
 * the sample text changes.
 */
import { writeFileSync, mkdirSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));

const CHAPTERS = [
  {
    title: 'Chapter 1',
    paragraphs: [
      'The Time Traveller (for so it will be convenient to speak of him) was expounding a recondite matter to us. His pale grey eyes shone and twinkled, and his usually pale face was flushed and animated.',
      'You must follow me carefully. I shall have to controvert one or two ideas that are almost universally accepted. The geometry, for instance, they taught you at school is founded on a misconception.',
      'Is not that rather a large thing to expect us to begin upon? said Filby, an argumentative person with red hair.',
    ],
  },
  {
    title: 'Chapter 2',
    paragraphs: [
      'The thing the Time Traveller held in his hand was a glittering metallic framework, scarcely larger than a small clock, and very delicately made. There was ivory in it, and some transparent crystalline substance.',
      'This little affair, said the Time Traveller, resting his elbows upon the table and pressing his hands together above the apparatus, is only a model. It is my plan for a machine to travel through time.',
    ],
  },
  {
    title: 'Chapter 3',
    paragraphs: [
      'I told some of you last Thursday of the principles of the Time Machine, and showed you the actual thing itself, incomplete in the workshop. There it is now, a little travel-worn, truly.',
      'I expected to finish it on Friday; but on Friday, when the putting together was nearly done, I found that one of the nickel bars was exactly one inch too short, and this I had to get remade.',
    ],
  },
];

const PAGE_W = 612;
const PAGE_H = 792;
const MARGIN = 72;
const BODY_SIZE = 12;
const HEADING_SIZE = 22;
const LINE_HEIGHT = 16;
const MAX_CHARS = 78;

function wrap(text, maxChars) {
  const words = text.split(' ');
  const lines = [];
  let line = '';
  for (const word of words) {
    if (line.length + word.length + 1 > maxChars && line) {
      lines.push(line);
      line = word;
    } else {
      line = line ? `${line} ${word}` : word;
    }
  }
  if (line) lines.push(line);
  return lines;
}

function escapePdf(text) {
  return text.replace(/([()\\])/g, '\\$1');
}

// Lay chapters out into pages of text operations.
const pages = [];
let ops = [];
let y = PAGE_H - MARGIN;
function newPage() {
  if (ops.length) pages.push(ops);
  ops = [];
  y = PAGE_H - MARGIN;
}
for (const chapter of CHAPTERS) {
  newPage();
  ops.push({ x: MARGIN, y, size: HEADING_SIZE, text: chapter.title });
  y -= LINE_HEIGHT * 2.5;
  for (const paragraph of chapter.paragraphs) {
    for (const line of wrap(paragraph, MAX_CHARS)) {
      if (y < MARGIN) newPage();
      ops.push({ x: MARGIN, y, size: BODY_SIZE, text: line });
      y -= LINE_HEIGHT;
    }
    y -= LINE_HEIGHT * 0.6;
  }
}
newPage();

const objects = [];
const pageRefs = pages.map((_, i) => `${4 + i * 2} 0 R`).join(' ');
objects.push(`1 0 obj\n<< /Type /Catalog /Pages 2 0 R >>\nendobj\n`);
objects.push(`2 0 obj\n<< /Type /Pages /Kids [${pageRefs}] /Count ${pages.length} >>\nendobj\n`);
objects.push(`3 0 obj\n<< /Type /Font /Subtype /Type1 /BaseFont /Times-Roman >>\nendobj\n`);
pages.forEach((pageOps, i) => {
  const pageNum = 4 + i * 2;
  const stream = pageOps
    .map((op) => `BT /F1 ${op.size} Tf ${op.x} ${op.y} Td (${escapePdf(op.text)}) Tj ET`)
    .join('\n');
  objects.push(
    `${pageNum} 0 obj\n<< /Type /Page /Parent 2 0 R /MediaBox [0 0 ${PAGE_W} ${PAGE_H}] /Resources << /Font << /F1 3 0 R >> >> /Contents ${pageNum + 1} 0 R >>\nendobj\n`,
  );
  objects.push(
    `${pageNum + 1} 0 obj\n<< /Length ${stream.length} >>\nstream\n${stream}\nendstream\nendobj\n`,
  );
});

let body = '%PDF-1.4\n';
const offsets = [];
for (const object of objects) {
  offsets.push(body.length);
  body += object;
}
const xrefStart = body.length;
let xref = `xref\n0 ${objects.length + 1}\n0000000000 65535 f \n`;
for (const offset of offsets) xref += `${String(offset).padStart(10, '0')} 00000 n \n`;
body += `${xref}trailer\n<< /Size ${objects.length + 1} /Root 1 0 R >>\nstartxref\n${xrefStart}\n%%EOF\n`;

const base64 = Buffer.from(body, 'latin1').toString('base64');
const out = `// GENERATED by scripts/make-sample-pdf.mjs — a tiny public-domain sample
// (H. G. Wells, "The Time Machine", excerpt) used by "Try a sample book".
export const SAMPLE_PDF_TITLE = 'The Time Machine (Sample)';
export const SAMPLE_PDF_BASE64 =
  '${base64}';
`;
const target = join(here, '../src/assets/samplePdf.ts');
mkdirSync(dirname(target), { recursive: true });
writeFileSync(target, out);
console.log(`make-sample-pdf: ${pages.length} pages, ${(base64.length / 1024).toFixed(0)}KB base64 → ${target}`);
